package grupo7.rentacar.Extras;

/**
 *
 * @author geova
 */
public class Extra {
    private String nombre;
    private int Id;

    public Extra(String nombre, int id) {
        this.nombre = nombre;
        this.Id = id;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public int getId() {
        return Id;
    }

    public void setId(int Id) {
        this.Id = Id;
    }
    
}
